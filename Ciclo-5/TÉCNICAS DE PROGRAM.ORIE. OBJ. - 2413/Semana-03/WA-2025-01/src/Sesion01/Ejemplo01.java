package Sesion01;

public class Ejemplo01 {

    /**
     * Java es case sensitive: distingue las mayusculas y minusculas
     * Java es diferente JAVA
     * todo programa de Java debe tener al menos una clase
     * tener cuidado con el orden de las llaves: deben abrirse y cerrarse en el mismo orden
     * el metodo main es el punto de entrada a un programa ejecutable
     * el metodo main es donde inicia y donde finaliza la ejecucion de un programa
     * el metodo main no tiene devolucion de datos
     * el metodo main acepta un parametro/argumento: una matriz de String
     * si el compilador encuentra algun error, debe ser corregido para que 
     * se ejecute el programa
     */
    
    public static void main(String[] args) {
        System.out.print("Esta es una linea de texto\n");
        System.out.println("Esta es otra linea de texto");
    }
    
}
